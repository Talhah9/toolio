// Returns the localised display fields for a tool.
// France-only tools always return French regardless of lang.
export function getToolText(tool, lang) {
  const l = tool.franceOnly ? 'fr' : lang;
  return {
    name:     tool[`name_${l}`]     || tool.name_en,
    short:    tool[`short_${l}`]    || tool.short_en,
    desc:     tool[`desc_${l}`]     || tool.desc_en,
    intro:    tool[`intro_${l}`]    || tool.intro_en    || tool[`desc_${l}`] || tool.desc_en,
    features: tool[`features_${l}`] || tool.features_en || [],
  };
}

